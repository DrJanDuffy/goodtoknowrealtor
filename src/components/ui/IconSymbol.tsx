export type IconSymbolKey = keyof typeof ICON_MAP;

interface IconSymbolProps {
  symbol: IconSymbolKey;
  className?: string;
  size?: number;
  strokeWidth?: number;
  ariaLabel?: string;
}

export function IconSymbol({
  symbol,
  className,
  size = 24,
  strokeWidth = 2,
  ariaLabel,
}: IconSymbolProps) {
  const IconComponent = ICON_MAP[symbol];

  if (!IconComponent) {
    return (
      <span className={className} role="img" aria-label={ariaLabel}>
        {symbol}
      </span>
    );
  }

  const mergedClassName = ['shrink-0', className].filter(Boolean).join(' ');

  return (
    <IconComponent
      className={mergedClassName}
      size={size}
      strokeWidth={strokeWidth}
      aria-hidden={ariaLabel ? undefined : true}
      aria-label={ariaLabel}
    />
  );
}
